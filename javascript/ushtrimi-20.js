/*
	Write a program that checks:
	•	if x is greater than y
	•	if x is not equal to y
	•	combine two conditions with && and ||
	•	Try out ++ and -- operators on a number.
*/

let x = 14;
let z = 20;

console.log(x > z);
console.log(x != z);

console.log((x > z) && (x != z));
console.log((x > z) || (x != z));

x++
z++
x--
z--

console.log(x);
console.log(z);